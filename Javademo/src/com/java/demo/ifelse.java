package com.java.demo;

public class ifelse {

	public static void main(String[] args) {
		
		int x = 30;   // 数据类型 变量 赋值 30

	    if( x < 20 )  // if(布尔表达式)
	        System.out.print("这是 if 语句");      //如果布尔表达式的值为true
	    //else
	        System.out.print("这是 else 语句 \n");  //如果布尔表达式的值为false
	    
	    System.out.println("wwwwwwwwww");
	    

	}

}
